#Arithmetic Operators
print("5+5 is ", 5+5)
print("5-5 is ", 5-5)
print("5/5 is ", 5/5)
print("5*5 is ", 5*5)
print("5**5 is ", 5**5)
print("15//6 is ", 15//6)
print("5%5 is ", 5%5)

#assignment Operators

x=5
print(x)

#Comparison Operators
i=8
print(i!=5)
print(i<=5)
print(i>=5)

#Logical Operators
a=True
b=False
print(a and b)
print(a or b)

#identity Operators
print(a is not b)
print(5 is not a)

#Membership Operators
list=[3,2,1,4,56,4,334,22,32]
